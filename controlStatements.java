public class controlStatements {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 4) {
            System.out.println("hello");

            int j = 1;
            while (j <= 3) {
                System.out.println("hii");
                j++;
            }

            i++;
        }
        System.out.println(i);
    }
}
